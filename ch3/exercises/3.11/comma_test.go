package comma3

import "testing"

func TestComma(t *testing.T) {
	cases := map[string]string{
		"":        "",
		"1":       "1",
		"123":     "123",
		"1234":    "1,234",
		"123456":  "123,456",
		"1234567": "1,234,567",

		"-1":       "-1",
		"-123":     "-123",
		"-1234":    "-1,234",
		"-123456":  "-123,456",
		"-1234567": "-1,234,567",

		"1.0":       "1.0",
		"123.0":     "123.0",
		"1234.0":    "1,234.0",
		"123456.0":  "123,456.0",
		"1234567.0": "1,234,567.0",

		"-1.0":       "-1.0",
		"-123.0":     "-123.0",
		"-12.34":     "-12.34",
		"-123.456":   "-123.456",
		"-1234567.0": "-1,234,567.0",
	}
	for k, v := range cases {
		actual := comma(k)
		if actual != v {
			t.Errorf("Expected: %v, but actual: %v", v, actual)
		}
	}
}
